/**
 * Методы расчёта расстояний для разных типов транспорта
 * 
 * Каждый тип транспорта использует свой метод расчёта:
 * - Haversine: для авиа (расстояние по прямой)
 * - OSRM: для автобусов (расстояние по дорогам)
 * - River Path: для паромов (расстояние по рекам)
 * - Rail Path: для ЖД (расстояние по железным дорогам)
 * - Manual: для фиксированных маршрутов (зимники, короткие маршруты)
 */
export enum DistanceCalculationMethod {
  /**
   * Формула Haversine - расчёт расстояния по прямой между двумя точками на сфере
   * Используется для: авиа
   */
  HAVERSINE = 'haversine',

  /**
   * OSRM API - расчёт расстояния по дорогам
   * Используется для: автобусы, такси
   */
  OSRM = 'osrm',

  /**
   * Расстояние вдоль русла реки
   * Используется для: паромы
   */
  RIVER_PATH = 'river_path',

  /**
   * Расстояние по железнодорожным линиям
   * Используется для: поезда
   */
  RAIL_PATH = 'rail_path',

  /**
   * Фиксированное расстояние (вручную заданное)
   * Используется для: зимники, короткие маршруты
   */
  MANUAL = 'manual',
}




